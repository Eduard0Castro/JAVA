package piratear;

public class MapaTesouro  extends Mapa{
    private int moedas, joias; 
    private Ilha ilha;
    
    public int getMoedas() {
        return moedas;
    }
    public void setMoedas(int moedas) {
        this.moedas = moedas;
    }
    public int getJoias() {
        return joias;
    }
    public void setJoias(int joias) {
        this.joias = joias;
    }
    public Ilha getIlha() {
        return ilha;
    }
    public void setIlha(Ilha ilha) {
        this.ilha = ilha;
    }

    
}
