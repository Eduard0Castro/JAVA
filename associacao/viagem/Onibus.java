package associacao.viagem;

public class Onibus {
    private String placa;
    private int bagageiro, anoFabrica, acentos, poltronas;
    
    public String getPlaca() {
        return placa;
    }
    public void setPlaca(String placa) {
        this.placa = placa;
    }
    public int getBagageiro() {
        return bagageiro;
    }
    public void setBagageiro(int bagageiro) {
        this.bagageiro = bagageiro;
    }
    public int getAnoFabrica() {
        return anoFabrica;
    }
    public void setAnoFabrica(int anoFabrica) {
        this.anoFabrica = anoFabrica;
    }
    public int getAcentos() {
        return acentos;
    }
    public void setAcentos(int acentos) {
        this.acentos = acentos;
    }
    public int getPoltronas() {
        return poltronas;
    }
    public void setPoltronas(int poltronas) {
        this.poltronas = poltronas;
    }


}
